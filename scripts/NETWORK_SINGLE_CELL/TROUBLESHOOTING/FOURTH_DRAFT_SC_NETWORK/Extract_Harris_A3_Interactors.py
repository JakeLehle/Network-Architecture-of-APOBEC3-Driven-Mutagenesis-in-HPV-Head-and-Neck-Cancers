MMC15_PATH = "/master/jlehle/WORKING/2026_NMF_PAPER/data/FIG_4/00_input/mmc15.xlsx"

# Output
OUTPUT_DIR = "/master/jlehle/WORKING/2026_NMF_PAPER/data/FIG_4/00_input"
