"""
Extract_Harris_A3_Interactors.py
================================

Extracts high-confidence APOBEC3 protein interactors from two Harris lab
publications and generates a combined reference gene list for cross-referencing
with network communities in Figure 4.

Source 1: Jang et al. (2024) Mol Cell Proteomics 23:100755
  "Protein Interaction Map of APOBEC3 Enzyme Family Reveals
   Deamination-Independent Role in Cellular Function"
  - Supplementary Table: mmc15.xlsx (AP-MS with SAINT/CompPASS scoring)
  - Full A3 family interactome (A3A through A3H)

Source 2: McCann et al. (2023) Nat Genet 55:1721-1734
  "APOBEC3B regulates R-loops and promotes transcription-associated
   mutagenesis in cancer"
  - ~24 high-confidence A3B interactors from AP-MS (Figure 1a,b)
  - 60% overlap with S9.6 (R-loop) AP-MS datasets
  - Manually curated from paper figures and text

Filtering criteria (Jang et al.):
  Step 1: SAINT BFDR < 0.05
  Step 2: CompPASS wd_percentile_perBait >= 0.90
  (Two-step filtering as described in their Methods)

Output:
  Harris_A3_interactors.txt — combined, deduplicated gene list with source
  annotations and functional categories

Usage:
  Place mmc15.xlsx in the same directory, then run:
    conda run -n NETWORK python Extract_Harris_A3_Interactors.py

Author: Jake Lehle
Texas Biomedical Research Institute
"""

